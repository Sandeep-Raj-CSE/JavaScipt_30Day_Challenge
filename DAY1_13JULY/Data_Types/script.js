// Task 4: Create variables of different data types 
//(number, string, boolean, object, array) and log each variable's type using the typeof operator.


const num = 12;
const string = "String";

const bool = true;

const obj = {
    name:"sandeep",
    roll:20
}

let arr = [1, 2, 3, 4, 5];

console.log(typeof num); // Output: number

console.log(typeof string); // Output: string

console.log(typeof bool); // Output: boolean

console.log(typeof obj); // Output: object

console.log(typeof(arr)); // Output: object (since arrays are objects in JavaScript)

